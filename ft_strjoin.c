#include "libft.h"

char		*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	res = !s1 || !s2 ? NULL : ft_strnew(ft_strlen((char*)s1) +
		ft_strlen((char*)s2));
	if (!res || !s1 || !s2)
		return (NULL);
	while (s1[i])
		res[j++] = s1[i++];
	i = 0;
	while (s2[i])
		res[j++] = s2[i++];
	return (res);
}
